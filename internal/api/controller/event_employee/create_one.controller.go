package event_employee_controller

import (
	"bookify/internal/domain"
	"fmt"
	"github.com/gin-gonic/gin"
	"net/http"
)

// CreateOne godoc
// @Summary Create a new event employee
// @Description Creates a new event employee for the current user
// @Tags Event Employees
// @Accept json
// @Produce json
// @Param eventEmployee body domain.EventEmployeeInput true "Event Employee Body"
// @Router /api/v1/event-employee/create [post]
func (e EventEmployeeController) CreateOne(ctx *gin.Context) {
	currentUser, exist := ctx.Get("currentUser")
	if !exist {
		ctx.JSON(http.StatusUnauthorized, gin.H{
			"status":  "error",
			"message": "You are not login!",
		})
		return
	}

	//  Lấy thông tin từ request
	var eventEmployeeInput domain.EventEmployeeInput
	if err := ctx.ShouldBindJSON(&eventEmployeeInput); err != nil {
		ctx.JSON(http.StatusBadRequest, gin.H{
			"status":  "error",
			"message": err.Error()},
		)
		return
	}

	err := e.EventEmployeeUseCase.CreateOne(ctx, &eventEmployeeInput, fmt.Sprintf("%s", currentUser))
	if err != nil {
		ctx.JSON(http.StatusInternalServerError, gin.H{
			"status":  "error",
			"message": err.Error()},
		)
		return
	}

	// Trả về phản hồi thành công
	ctx.JSON(http.StatusOK, gin.H{
		"status": "success",
	})
}
